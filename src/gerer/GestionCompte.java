    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Ajout Bibliothecaire Inserer Donnees
        Matcher matcher = idPattern.matcher(jTextField5.getText());
        boolean matchFound = matcher.find();
        if(!matchFound) {
            jLabel16.setText("ID invalide");
        }
        else {
            jLabel16.setText("");
            try{
                Statement stmt = con.createStatement();
                String query = "SELECT * from bibliothecaire WHERE id_biblio="+jTextField5.getText()+"";
                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()){
                    updateBiblio = new Bibliothecaire(Integer.parseInt(jTextField5.getText()),con);
                    jTextField6.setText(updateBiblio.getNom());
                    jTextField7.setText(updateBiblio.getPrenom());
                    jTextField8.setText(updateBiblio.getLogin());
                    updateCompte = new Compte(updateBiblio.getLogin(), con);
                    jTextField9.setText(updateCompte.getMdp());
                }
                else{
                    jLabel16.setText("Aucun bibliothecaire avec cette id");
                }
            }
            catch(SQLException err){
                System.out.println("Erreur insertion bibliothecaire");
                System.out.println(err.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

