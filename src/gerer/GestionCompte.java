    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // Confirmer MAJ Adh
        jLabel13.setText("");jLabel15.setText("");jLabel20.setText("");jLabel22.setText("");jLabel16.setText("");jLabel18.setText("");
        Boolean t=true;
        String inputNom = jTextField6.getText();
        String inputPrenom = jTextField7.getText();
        String inputLogin = jTextField8.getText();
        String inputMdp = jTextField9.getText();
        Matcher matcher;
        
        if (updateBiblio==null){
            jLabel16.setText("Aucun bibliothecaire choisi");
            return;
        }
        
        matcher = nomPattern.matcher(inputNom);
        if(!matcher.find()) {
            t=false;
            jLabel13.setText("Nom invalide");
        }
        matcher = nomPattern.matcher(inputPrenom);
        if(!matcher.find()) {
            t=false;
            jLabel15.setText("Prenom invalide");
        }
        matcher = loginPattern.matcher(inputLogin);
        if(!matcher.find()) {
            t=false;
            jLabel20.setText("Login invalide");
        }
        matcher = passwordPattern.matcher(inputMdp);
        if(!matcher.find()) {
            t=false;
            jLabel22.setText("Mot de passe invalide");
        }
        if (t){
            try{
                Statement stmt = con.createStatement();
                String query1 = "SELECT * FROM compte WHERE login='"+inputLogin+"'";
                ResultSet rs = stmt.executeQuery(query1);
                if (rs.next() && !updateBiblio.getLogin().equals(inputLogin)){
                    t=false;
                    jLabel22.setText("Login deja utilisé");
                }
            }
            catch (SQLException err){
                System.out.println("Erreur ajout");
                System.out.println(err.getMessage());
            }
        }
        if (t){
            updateBiblio.setNom(jTextField6.getText());
            updateBiblio.setPrenom(jTextField7.getText());
            if (!inputLogin.equals(updateBiblio.getLogin())){
                String oldLogin = updateBiblio.getLogin();
                Compte compte = new Compte(inputLogin, inputMdp, con);
                updateBiblio.setLogin(inputLogin);
                updateBiblio.updateDB(con);
                Compte.delDB(oldLogin, con);
            }
            else{
                updateCompte.setMdp(inputMdp);
                updateCompte.updateDB(con);
                updateBiblio.updateDB(con);
            }
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Supprimer Adh
        jLabel16.setText("");
        Matcher matcher = idPattern.matcher(jTextField5.getText());
        boolean matchFound = matcher.find();
        if(!matchFound) {
            jLabel16.setText("ID invalide");
            return;
        }
        try{
            Statement stmt = con.createStatement();
            String query = "SELECT * from bibliothecaire WHERE id_biblio="+Integer.parseInt(jTextField5.getText());
            ResultSet rs = stmt.executeQuery(query);
            if (rs.next()){
                int input = JOptionPane.showConfirmDialog(null, "Confirmer la supression","Confirmation",JOptionPane.OK_CANCEL_OPTION);
                if (input==0){
                    jLabel16.setText("");
                    Bibliothecaire biblio = new Bibliothecaire(Integer.parseInt(jTextField5.getText()), con);
                    Bibliothecaire.delDB(Integer.parseInt(jTextField5.getText()), con);
                    Compte.delDB(biblio.getLogin(), con);
                    JOptionPane.showMessageDialog(null, "Bibliothecaire supprimé avec succès");
                }
            }
            else{
                jLabel16.setText("Aucun bibliothecaire avec cette id");
            }
        }
        catch(SQLException err){
            System.out.println("Erreur suppression bibliothecaire");
            System.out.println(err.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

