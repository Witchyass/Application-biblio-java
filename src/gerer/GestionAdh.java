    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Ajout Adherant Inserer Donnees
        Matcher matcher = idPattern.matcher(jTextField5.getText());
        boolean matchFound = matcher.find();
        if(!matchFound) {
            jLabel16.setText("ID invalide");
        }
        else {
            jLabel16.setText("");
            try{
                Statement stmt = con.createStatement();
                String query = "SELECT * from adherent WHERE id_adhe="+jTextField5.getText()+"";
                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()){
                    updateAdh = new Adherent(Integer.parseInt(jTextField5.getText()),con);
                    jTextField6.setText(updateAdh.getNom());
                    jTextField7.setText(updateAdh.getPrenom());
                    jTextField8.setText(updateAdh.getEmail());
                    jTextField9.setText(String.valueOf(updateAdh.getTel()));
                }
                else{
                    jLabel16.setText("Aucun adherent avec cette id");
                }
            }
            catch(SQLException err){
                System.out.println("Erreur insertion adherent");
                System.out.println(err.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

