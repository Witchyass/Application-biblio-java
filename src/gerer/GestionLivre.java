    public final void getAuteurs(){
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        listeAuteurs = new ArrayList<>();
        try {
            Statement stmt = con.createStatement();
            String query = "SELECT * from auteur";
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()){
                jComboBox1.addItem(rs.getInt("id_auteur")+": "+rs.getString("nomAut")+" "+rs.getString("prenomAut"));
                jComboBox2.addItem(rs.getInt("id_auteur")+": "+rs.getString("nomAut")+" "+rs.getString("prenomAut"));
                listeAuteurs.add(rs.getInt("id_auteur")+": "+rs.getString("nomAut")+" "+rs.getString("prenomAut"));
            }
            arrayAuteurs = new String[ listeAuteurs.size() ];
            listeAuteurs.toArray( arrayAuteurs );
        } 
        catch (SQLException err) {
            System.out.println(err.getMessage());
        }
    }
    

