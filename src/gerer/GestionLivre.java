    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Maj Livre Inserer Donnees
        jLabel16.setText("");
        Matcher matcher = isbnPattern.matcher(jTextField5.getText());
        boolean matchFound = matcher.find();
        if(!matchFound) {
            jLabel16.setText("ISBN invalide");
        }
        else {
            jLabel16.setText("");
            try{
                Statement stmt = con.createStatement();
                String query = "SELECT * from livre WHERE isbn='"+jTextField5.getText()+"'";
                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()){
                    updateLivre = new Livre(jTextField5.getText(),con);
                    jTextField7.setText(updateLivre.getIsbn());
                    jTextField8.setText(updateLivre.getTitre());
                    jSpinner2.setValue(Livre.getNombreExemplaire(updateLivre.getIsbn(), con));
                    for (int i = 0; i < arrayAuteurs.length; i++) {
                        if(updateLivre.id_auteur==Integer.parseInt(String.valueOf(arrayAuteurs[i]).substring(0, 1))){
                            jComboBox1.setSelectedIndex(i);
                        }
                        
                    }
                }
                else{
                    jLabel16.setText("Aucun livre avec ce ISBN");
                }
            }
            catch(SQLException err){
                System.out.println("Erreur insertion adherent");
                System.out.println(err.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

