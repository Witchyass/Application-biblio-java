    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // Confirmer MAJ Adh
        jLabel15.setText("");jLabel21.setText("");jLabel18.setText("");jLabel16.setText("");jLabel20.setText("");
        Boolean t=true;
        Matcher matcher;
        
        if (updateLivre==null){
            jLabel16.setText("Aucun livre choisi");
            return;
        }
        
        matcher = isbnPattern.matcher(jTextField7.getText());
        if(!matcher.find()) {
            t=false;
            jLabel15.setText("ISBN invalide");
        }
        matcher = titlePattern.matcher(jTextField8.getText());
        if(!matcher.find()) {
            t=false;
            jLabel21.setText("Titre invalide");
        }
        if(Integer.parseInt(jSpinner2.getValue().toString())<0) {
            t=false;
            jLabel20.setText("Nombre invalide");
        }
        
        if (t){
            if (Livre.getNombreExemplaire(updateLivre.getIsbn(), con)-Integer.parseInt(jSpinner2.getValue().toString())>Livre.getNombreExemplaireFree(updateLivre.getIsbn(), con)){
                jLabel20.setText("<html>Ce livre n'a pas "+(Livre.getNombreExemplaire(updateLivre.getIsbn(), con)-Integer.parseInt(jSpinner2.getValue().toString()))+"<br>exemplaires libres</html>");
                t=false;
            }
            try{
                Statement stmt = con.createStatement();
                String query1 = "SELECT * FROM livre WHERE isbn='"+jTextField7.getText()+"'";
                ResultSet rs = stmt.executeQuery(query1);
                if (rs.next() && !jTextField7.getText().equals(updateLivre.getIsbn())){
                    t=false;
                    jLabel15.setText("ISBN deja utilisé");
                }
            }
            catch (SQLException err){
                System.out.println("Erreur ajout");
                System.out.println(err.getMessage());
            }
        }
        if (t){
            updateLivre.setIsbn(jTextField7.getText());
            updateLivre.setTitre(jTextField8.getText());
            updateLivre.setAuteur(Integer.parseInt(jComboBox1.getSelectedItem().toString().substring(0, 1)));
            updateLivre.updateDB(con);
            int exempDiff=Integer.parseInt(jSpinner2.getValue().toString())-Livre.getNombreExemplaire(updateLivre.getIsbn(),con);
            if(exempDiff>0){
                for (int i = 0; i < exempDiff; i++) {
                    System.out.println(i);
                    Exemplaire exemp = new Exemplaire(0, updateLivre.getIsbn(), con);
                }
            }
            if(exempDiff<0){
                try {
                    Statement stmt = con.createStatement();
                    String query="SELECT id_exemp FROM exemplaire WHERE isbn='"+updateLivre.getIsbn()+"' AND etat_exemp=0";
                    ResultSet rs = stmt.executeQuery(query);
                    for (int i = 0; i < -exempDiff; i++) {
                        rs.next();
                        Exemplaire.delDB(rs.getInt("id_exemp"), con);
                    }
                }
                catch (SQLException err) {
                    System.out.println(err.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

